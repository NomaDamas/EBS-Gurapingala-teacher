import { readFileSync } from "node:fs";

const failures = [];

const externalReviewDecision = normalizeDecision(process.env.EXTERNAL_REVIEW_DECISION);
const verifyDeployStatus = normalizeStatus(process.env.VERIFY_DEPLOY_STATUS);
const workerUrl = String(process.env.WORKER_URL || process.env.WORKER_HEALTH_URL || "").trim();
const prHeadSha = String(process.env.PR_HEAD_SHA || process.env.GITHUB_SHA || "").trim();
const expectedHeadSha = String(process.env.EXPECTED_PR_HEAD_SHA || "").trim();
const ciStatus = normalizeStatus(process.env.CI_STATUS || process.env.GITHUB_CI_STATUS);
const requireOpenAI = process.env.REQUIRE_OPENAI !== "false";
const requireTeacherToken = process.env.REQUIRE_TEACHER_TOKEN !== "false";
const externalReviewFile = String(process.env.EXTERNAL_REVIEW_FILE || "").trim();
const verifyDeployEvidenceFile = String(process.env.VERIFY_DEPLOY_EVIDENCE_FILE || "").trim();

if (externalReviewDecision !== "approve") {
  failures.push("EXTERNAL_REVIEW_DECISION=APPROVE is required before merge/release");
}

if (verifyDeployStatus !== "pass") {
  failures.push("VERIFY_DEPLOY_STATUS=pass is required after running npm run verify:deploy against the real Worker URL");
}

if (!isHttpsUrl(workerUrl)) {
  failures.push("WORKER_URL or WORKER_HEALTH_URL must be the real https Cloudflare Worker URL");
}

if (!prHeadSha) {
  failures.push("PR_HEAD_SHA or GITHUB_SHA is required so review/deploy evidence is tied to a commit");
}

if (expectedHeadSha && prHeadSha && expectedHeadSha !== prHeadSha) {
  failures.push("EXPECTED_PR_HEAD_SHA does not match PR_HEAD_SHA; rerun review/deploy verification on the latest commit");
}

if (ciStatus !== "pass" && ciStatus !== "success") {
  failures.push("CI_STATUS=pass or CI_STATUS=success is required for the latest PR head");
}

if (requireOpenAI && process.env.REQUIRE_OPENAI !== "true") {
  failures.push("REQUIRE_OPENAI=true must be recorded for production release verification");
}

if (requireTeacherToken && process.env.REQUIRE_TEACHER_TOKEN !== "true") {
  failures.push("REQUIRE_TEACHER_TOKEN=true must be recorded for production release verification");
}

const externalReview = readJsonEvidence(externalReviewFile, "EXTERNAL_REVIEW_FILE");
if (!externalReviewFile) {
  failures.push("EXTERNAL_REVIEW_FILE is required and must contain the external GPT-5.5 xhigh/equivalent review decision");
} else if (externalReview) {
  if (normalizeDecision(externalReview.decision || externalReview.reviewDecision) !== "approve") {
    failures.push("EXTERNAL_REVIEW_FILE decision must be APPROVE");
  }
  if (!externalReview.prHeadSha || externalReview.prHeadSha !== prHeadSha) {
    failures.push("EXTERNAL_REVIEW_FILE prHeadSha must match PR_HEAD_SHA");
  }
  if (!String(externalReview.reviewer || externalReview.model || "").trim()) {
    failures.push("EXTERNAL_REVIEW_FILE must identify the external reviewer/model");
  }
}

const deployEvidence = readJsonEvidence(verifyDeployEvidenceFile, "VERIFY_DEPLOY_EVIDENCE_FILE");
if (!verifyDeployEvidenceFile) {
  failures.push("VERIFY_DEPLOY_EVIDENCE_FILE is required and must be generated by npm run verify:deploy against the real Worker URL");
} else if (deployEvidence) {
  if (deployEvidence.schemaVersion !== "deploy-verification-evidence/v1") {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE schemaVersion must be deploy-verification-evidence/v1");
  }
  if (normalizeStatus(deployEvidence.status) !== "pass") {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE status must be pass");
  }
  if (!deployEvidence.prHeadSha || deployEvidence.prHeadSha !== prHeadSha) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE prHeadSha must match PR_HEAD_SHA");
  }
  if (normalizeBaseUrl(deployEvidence.workerUrl) !== normalizeBaseUrl(workerUrl)) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE workerUrl must match WORKER_URL");
  }
  if (deployEvidence.requireOpenAI !== true) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE must record requireOpenAI=true");
  }
  if (deployEvidence.requireTeacherToken !== true) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE must record requireTeacherToken=true");
  }
  if (!Number.isFinite(deployEvidence.totalChecks) || deployEvidence.totalChecks < 18) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE must include all deploy verification checks");
  }
  if (deployEvidence.passedChecks !== deployEvidence.totalChecks) {
    failures.push("VERIFY_DEPLOY_EVIDENCE_FILE passedChecks must equal totalChecks");
  }
}

if (failures.length) {
  for (const failure of failures) console.error(`FAIL ${failure}`);
  console.error(`release audit failed: ${failures.length} issue(s)`);
  process.exit(1);
}

console.log("release audit passed");
console.log(`prHeadSha=${prHeadSha}`);
console.log(`workerUrl=${workerUrl}`);
console.log(`externalReviewDecision=${externalReviewDecision}`);
console.log(`verifyDeployStatus=${verifyDeployStatus}`);
console.log(`externalReviewFile=${externalReviewFile}`);
console.log(`verifyDeployEvidenceFile=${verifyDeployEvidenceFile}`);

function normalizeDecision(value) {
  return String(value || "").trim().toLowerCase().replace(/[^a-z_]/g, "");
}

function normalizeStatus(value) {
  return String(value || "").trim().toLowerCase();
}

function isHttpsUrl(value) {
  try {
    const url = new URL(value);
    return url.protocol === "https:" && Boolean(url.hostname);
  } catch {
    return false;
  }
}

function normalizeBaseUrl(value) {
  try {
    const url = new URL(String(value || ""));
    url.pathname = "/";
    url.search = "";
    url.hash = "";
    return url.toString();
  } catch {
    return "";
  }
}

function readJsonEvidence(file, label) {
  if (!file) return null;
  try {
    return JSON.parse(readFileSync(file, "utf8"));
  } catch (error) {
    failures.push(`${label} must be readable JSON: ${error instanceof Error ? error.message : String(error)}`);
    return null;
  }
}
